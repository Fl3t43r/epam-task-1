console.log('String + boolean:', '5' + true); 
console.log('String + number:', '5' + 10);    
console.log('Number + boolean:', 10 + true);  

console.log('String * boolean:', '5' * true); 
console.log('String * number:', '5' * 10);    
console.log('Number * boolean:', 10 * true);  

console.log('String / boolean:', '10' / true); 
console.log('String / number:', '10' / 2);    
console.log('Number / boolean:', 10 / true);  

console.log('String to number:', Number('10'));     
console.log('String to boolean:', Boolean('true')); 
console.log('Number to string:', String(5));        
